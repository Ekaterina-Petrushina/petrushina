/*Упражнение 3.1(1.10). Кроме оператора ++, который добавляет 1 к своему
операнду, существует оператор декремента (--), который вычитает 1.
Используйте оператор декремента, чтобы написать цикл while,
выводящий на экран числа от десяти до нуля.*/
#include <iostream>
using std::cout;
using std::endl;
int main() {
    int val = 10;
    while (val >= 0) {
        cout << val <<endl;
        --val;
}
return 0;
}